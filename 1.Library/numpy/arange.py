import numpy as np


print("Array from 0 to 9 using np.arange(10):")
arr1 = np.arange(10)
print(arr1)

print("Array from 1 to 9 using np.arange(1, 10):")
arr2 = np.arange(1, 10)
print(arr2)

print("Array from 0 to 9 with a step of 2 using np.arange(0, 10, 2):")
arr3 = np.arange(0, 10, 2)
print(arr3)

print("Array from 0 to 1 with a step of 0.2 using np.arange(0, 1, 0.2):")
arr4 = np.arange(0, 1, 0.2)
print(arr4)

print("Array from 10 to 1 with a step of -1 using np.arange(10, 0, -1):")
arr5 = np.arange(10, 0, -1)
print(arr5)
